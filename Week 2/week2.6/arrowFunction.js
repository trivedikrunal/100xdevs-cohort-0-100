// Normal Function Two sum
function sum(a,b){
    return a+b;
}
let ansNormal = sum(1,2)
console.log(ansNormal); // Output: 3

//------------------------------------------

// Arrow Function Two sum
let sumA = (a,b) =>{
    return a+b;
}
let ansArrow = sumA(1,2);
console.log(ansArrow); // Output: 3
